#include "builtins.h"

int	ft_env(t_env *env)
{
	int	i;

	i = 0;
	if (!env || !env->vars)
		return (1);
	while (i < env->size_vars)
	{
		printf("%s\n", env->vars[i]);
		i++;
	}
	return (0);
}
