#include "libft.h"

/** UPDATED VERSION
 * @note
 * Compare until one of the strings ends
 * Compare null terminators if necessary
 */
int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}
